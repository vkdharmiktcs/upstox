package variables1;

public class Local_variable1 {//class body open

	public static void main(String[] args) {//method body open
		int i=50;
		System.out.println(i);//50
		
		byte a=40;
		System.out.println(a);//40
		
		short s=50;
		System.out.println(s);//50
		
		long b=254060325l;
		System.out.println(b);
		
		float d=52.2f;
		System.out.println(d);//52.2
		
		String v="Vinay";
		System.out.println(v);//vinay
		

	}//method body close

}//class body close
