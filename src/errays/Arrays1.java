package errays;

public class Arrays1 {
	public static void main(String [] args) {
		int  ar[]=new int [4];
		 ar[0]=10;
		ar[1]=20;
		ar[2]=30;
		ar[3]=40;
		// to print 10;
		System.out.println(ar[0]);
		// to print entire data
		for(int i=0;i<=3;i++) {
			System.out.println(ar[i]);
		}
	}

}
