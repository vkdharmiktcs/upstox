package logicalprogram;

public class Swapping_without {
	public static void main(String [] args) {
	System.out.println("before swapping");
	int a=10;
	int b=20;
	
	System.out.println(a+" "+b);
	int c=a;
	a=b;
	b=c;
	
	System.out.println("after swapping");
	System.out.println(a+" "+b);
	}

}
