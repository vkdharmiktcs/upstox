package variables1;

public class Global_variable1 {//class body open
	static int i=20;
	static int a=20;
	static byte b=5;
	static short s=30;
	static long v=2014352633l;
	static String c="Vivek";
	
			

	public static void main(String[] args) {//method body open
		System.out.println(i);
		System.out.println(a);
		System.out.println(b);
		System.out.println(s);
		System.out.println(v);
		System.out.println(c);
		
		

	}//method body closed

}//class body closed
