package scanner_class;

public class Swapping {
	public static void main(String [] args) {
		System.out.println("before swapping");
		int a=30;
		int b=40;
		System.out.println(a+"and"+b);
		int c=a;
		a=b;
		b=c;
		System.out.println("after swapping ");
		System.out.println(a+"and"+b);
	
	}

}
