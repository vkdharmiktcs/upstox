package logical_program1;

public class Swapping1 {
	public static void main(String [] args) {
		System.out.println("before swapping");
		int a=10;
		int b=20;
	System.out.println(a+""+b);
		System.out.println("after swapping");
	   int c=a;
	   a=b;
	   b=c;
	   System.out.println(a+""+b);
	   
	}

}
